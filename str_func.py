def str_func(string: str) -> str:
    """
    Возвращает текст большими буквами.
    :param string:
    :return:
    """
    return string.upper()


def str_title(string: str) -> str:
    """
    Возвращает текст, где первые буквы заглавные.
    :param string:
    :return:
    """
    return string.title()
